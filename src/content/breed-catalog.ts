const encode = (category: number, index: number): Record<Dimension, number> => {
  const base = [
    0.75, 0.55, 0.6, 0.7, 0.7, 0.75, 0.45, 0.85, 0.55, 0.6, 0.6, 0.55,
  ];
  if (category === 0) {
    base[1] = 0.65;
    base[2] = 0.75;
    base[10] = 0.8;
    base[6] = 0.35;
  }
  if (category === 1) {
    base[1] = 0.55;
    base[2] = 0.6;
    base[8] = 0.7;
    base[10] = 0.5;
  }
  if (category === 2) {
    base[1] = 0.3;
    base[2] = 0.35;
    base[6] = 0.85;
    base[8] = 0.8;
  }
  if (category === 3) {
    base[1] = 0.15;
    base[2] = 0.2;
    base[6] = 0.95;
    base[8] = 0.95;
    base[10] = 0.1;
  }
  const code = index;
  base[3] = (code % 5) / 4;
  base[9] = (Math.floor(code / 5) % 5) / 4;
  base[11] = (Math.floor(code / 25) % 5) / 4;
  return Object.fromEntries(
    dimensions.map((dimension, i) => [dimension, base[i]]),
  ) as Record<Dimension, number>;
};
const make = (
  entries: CatalogEntry[],
  category: number,
  petType: string,
  emoji: string,
): Recommendation[] =>
  entries.map(([name, size, note], index) => ({
    id: `${petType}-${index + 1}`,
    title: [
      "温柔陪伴型",
      "灵动探索型",
      "沉稳观察型",
      "聪明协作型",
      "独立自在型",
    ][index % 5],
    petType: `${petType} · ${size}`,
    emoji,
    example: `推荐品种：${name}`,
    ideal: encode(category, index),
    reason: `你的性格与生活节奏更接近 ${name} 的常见适配画像。${note}；品种只能提供参考，最终应以具体个体为准。`,
    checks: [],
  }));

export function buildRecommendations(checks: string[]): Recommendation[] {
  const results = [
    ...make(dogs, 0, "犬类", "🐕"),
    ...make(cats, 1, "猫类", "🐈"),
    ...make(smallPets, 2, "小型宠物", "🐹"),
    ...make(aquatic, 3, "水族宠物", "🐠"),
  ];
  for (const result of results) result.checks = checks;
  return results;
}
